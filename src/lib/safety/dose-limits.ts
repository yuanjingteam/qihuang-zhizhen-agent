/**
 * Check if a herb dose exceeds the pharmacopoeia limit
 */
export function checkDoseLimit(herb: string, doseGrams: number): {
  valid: boolean;
  limit: [number, number] | null;
  message: string | null;
} {
  const limit = PHARMACOPOEIA_DOSE_LIMITS[herb];
  if (!limit) {
    return { valid: true, limit: null, message: null };
  }

  const [min, max] = limit;
  if (doseGrams < min) {
    return {
      valid: false,
      limit,
      message: `${herb}剂量${doseGrams}g低于药典最低剂量${min}g`,
    };
  }
  if (doseGrams > max) {
    return {
      valid: false,
      limit,
      message: `${herb}剂量${doseGrams}g超过药典最高剂量${max}g`,
    };
  }

  return { valid: true, limit, message: null };
}
